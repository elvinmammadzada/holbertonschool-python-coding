#!/usr/bin/python3
"""
This module defines a class Square with private size.
Includes getter and setter with validation, and area method.
"""


class Square:
    """Square with private size, getter/setter, and area."""

    def __init__(self, size=0):
        self.size = size

    @property
    def size(self):
        """Retrieve the size."""
        return self.__size

    @size.setter
    def size(self, value):
        """Set the size with validation."""
        if not isinstance(value, int):
            raise TypeError("size must be an integer")
        if value < 0:
            raise ValueError("size must be >= 0")
        self.__size = value

    def area(self):
        """Return the area of the square."""
        return self.__size ** 2
